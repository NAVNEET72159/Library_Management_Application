    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel dtm2 = (DefaultTableModel)jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();
        int id = Integer.parseInt(dtm2.getValueAt(selectIndex, 0).toString());
        
        String memberId = txtMemberId.getText();
        String memberName = txtMemberName.getText();
        BookItem bItem = (BookItem) txtBook.getSelectedItem();
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String issueDate = date_format.format(txtIssuedDate.getDate());
        String returnDate = date_format.format(txtReturnDate.getDate());
        
        try {
            pst = con.prepareStatement("update lendbook set memberId = ?, memberName = ?, bookId = ?, bookedDate = ?, returnedDate = ? where id = ?");
            pst.setString(1, memberId);
            pst.setString(2, memberName);
            pst.setInt(3, bItem.id);
            pst.setString(4, issueDate);
            pst.setString(5, returnDate);
            pst.setInt(6, id);
            int k = pst.executeUpdate();
            
            if (k==1) {
                JOptionPane.showMessageDialog(this, "Book Updated");
                txtMemberId.setText("");
                txtMemberName.setText("");
                txtBook.setSelectedItem(-1);
                txtMemberId.requestFocus();
                IssuedBook_Load();
                jButton1.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(LendBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

