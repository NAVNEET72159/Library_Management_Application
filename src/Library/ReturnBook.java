    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String memberId = txtMemberId.getText();
        String memberName = txtMemberName.getText();
        String bookName = txtBook.getText();
        String returnDate = txtReturnedDate.getText();
        String elpdays = txtDateElapsed.getText();
        String fine = txtFine.getText();
        
        try {
            pst = con.prepareStatement("insert into returnedbook(memberId,memberName,bookName,returnDate,elapsedDate,fine)values(?,?,?,?,?,?)");
            pst.setString(1, memberId);
            pst.setString(2, memberName);
            pst.setString(3, bookName);
            pst.setString(4, returnDate);
            pst.setString(5, elpdays);
            pst.setString(6, fine);
            int k = pst.executeUpdate();
            
            pst = con.prepareStatement("delete from lendbook where memberId = ?");
            pst.setString(1, memberId);
            pst.executeUpdate();
            
            if (k==1) {
                
                JOptionPane.showMessageDialog(this, "Return Book Successful");
                txtMemberId.setText("");
                txtMemberName.setText("");
                txtBook.setText("");
                txtReturnedDate.setText("");
                txtDateElapsed.setText("");
                txtFine.setText("");
                txtMemberId.requestFocus();
                ReturnedBook_Load();
                
            } else {
                JOptionPane.showMessageDialog(this, "Error!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

