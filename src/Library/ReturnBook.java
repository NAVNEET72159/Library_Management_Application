    public void ReturnedBook_Load() {
        int c;
        try {
            pst = con.prepareStatement("select * from returnedbook");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("id"));
                    v2.add(rs.getString("memberId"));
                    v2.add(rs.getString("memberName"));
                    v2.add(rs.getString("bookName"));
                    v2.add(rs.getString("returnDate"));
                    v2.add(rs.getString("elapsedDate"));
                    v2.add(rs.getString("fine"));
                }
                dtm.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

