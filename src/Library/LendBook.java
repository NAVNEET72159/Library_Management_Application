    public class BookItem {
        int id;
        String name;
        
        public BookItem(int id, String name) {
            this.id = id;
            this.name = name;
        }
        public String toString() {
            return name;
        }
    }
    
    public void book() {
        try {
            pst = con.prepareStatement("select * from book");
            rs = pst.executeQuery();
            txtBook.removeAllItems();
            
            
            while (rs.next()) {
                txtBook.addItem(new LendBook.BookItem(rs.getInt(1), rs.getString(2)));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void IssuedBook_Load() {
        int c;
        try {
            pst = con.prepareStatement("select l.id, m.memberName, b.bookName, l.bookedDate, l.returnedDate from lendbook l JOIN member m ON l.memberId = m.id JOIN book b ON l.bookId = b.id");
            rs = pst.executeQuery();
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
            dtm.setRowCount(0);
            
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("l.id"));
                    v2.add(rs.getString("m.memberName"));
                    v2.add(rs.getString("b.bookName"));
                    v2.add(rs.getString("l.bookedDate"));
                    v2.add(rs.getString("l.returnedDate"));
                }
                dtm.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

