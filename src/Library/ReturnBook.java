    private void txtMemberIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMemberIdKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
        String id = txtMemberId.getText();
        
        try {
            pst = con.prepareStatement("select m.memberName, b.bookName, l.returnedDate, DATEDIFF(now(), l.returnedDate) as elap from lendbook l JOIN member m ON l.memberId = m.id JOIN book b ON l.bookId = b.id and l.memberId = ?");
            pst.setString(1, id);
            rs = pst.executeQuery();
            
            if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "Member ID not found.");
            } else {
                String memberName = rs.getString("m.MemberName");
                String bookName = rs.getString("b.bookName");
                txtMemberName.setText(memberName.trim());
                txtBook.setText(bookName.trim());
                String date = rs.getString("l.returnedDate");
                txtReturnedDate.setText(date);
                String elp = rs.getString("elap");
                
                int elapsed = Integer.parseInt(elp);
                
                if (elapsed > 0) {
                    txtDateElapsed.setText(elp);
                    int fine = elapsed * 100;
                    txtFine.setText(String.valueOf(fine));
                } else {
                    txtDateElapsed.setText("0");
                    txtFine.setText("0");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_txtMemberIdKeyPressed

